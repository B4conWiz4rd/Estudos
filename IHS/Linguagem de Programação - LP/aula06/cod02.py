#TAMANHO DA LISTA, MAIOR DA LISTA, MENOR VALOR DA LISTA, SOMA DE TODOS OS ELEMENTOS, LISTA EM OREDEM CRESCENTE
l = [5,7,2,9,4,1,3]
print ('Lista: ',l)
print ('Tamanho da lista:',(len(l)))
print ('Maior da lista:',(min(l)))
print ('Menor da lista:',(max(l)))
print ('Soma dos valores da lista: ',(sum(l)))
print ('Lista em ordem crecente: ',(sorted(l)))
print ('Lista em ordem decrecente: ',(sorted(l, reverse=True)))